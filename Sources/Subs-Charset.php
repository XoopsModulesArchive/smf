// Fixes corrupted serialized strings after a character set conversion.
function fix_serialized_columns()
{
    global $db_prefix;

    $request = db_query(
        "
		SELECT ID_ACTION, extra
		FROM {$db_prefix}log_actions
		WHERE action IN ('remove', 'delete')",
        __FILE__,
        __LINE__
    );

    while (false !== ($row = $GLOBALS['xoopsDB']->fetchArray($request))) {
        if (false === @unserialize($row['extra']) && 1 === preg_match('~^(a:3:{s:5:"topic";i:\d+;s:7:"subject";s:)(\d+):"(.+)"(;s:6:"member";s:5:"\d+";})$~', $row['extra'], $matches)) {
            db_query(
                "
				UPDATE {$db_prefix}log_actions
				SET extra = '$matches[1]" . mb_strlen($matches[3]) . ":\"$matches[3]\"$matches[4]'
				WHERE ID_ACTION = $row[ID_ACTION]
				LIMIT 1",
                __FILE__,
                __LINE__
            );
        }
    }

    $GLOBALS['xoopsDB']->freeRecordSet($request);

    // Refresh some serialized strings stored in the settings table.

    updateStats('calendar');

    // The memberlist cache needs to be recalculated too.

    updateSettings(['memberlist_updated' => time()]);
}
